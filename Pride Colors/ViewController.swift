import UIKit

class ViewController: UIViewController {

	@IBAction func turnRedOn(_ sender: Any) {
		view.backgroundColor = .red
	}
	
	@IBAction func turnOrangeOn(_ sender: Any) {
		view.backgroundColor = .orange
	}
	
	@IBAction func turnYellowOn(_ sender: Any) {
		view.backgroundColor = .yellow
	}
	
	
	
	@IBAction func turnLaSalleGreenOn(_ sender: Any) {
		view.backgroundColor = .green
	}
	
	
	@IBAction func turnBlueOn(_ sender: Any) {
		view.backgroundColor = .blue
	}
	
	
	@IBAction func turnPurpleOn(_ sender: Any) {
		view.backgroundColor = .purple
	}
	
	@IBAction func turnWhiteOn(_ sender: Any) {
		view.backgroundColor = .white
	}
	
	
}

